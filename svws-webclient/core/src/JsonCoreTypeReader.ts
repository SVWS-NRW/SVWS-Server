interface JsonCoreTypeEntry<T> {
	bezeichner: string;
	idHistorie: number;
	historie: T[];
}

interface JsonCoreTypeData<T> {
	version: number;
	daten: JsonCoreTypeEntry<T>[];
}

interface JsonCoreTypeDataResult<T> {
	version: number;
	mapData: HashMap<string, List<T>>;
	mapHistorienIDs: HashMap<string, number>;
}


